# backend/agents.py
"""
四个专职 Sub-Agent 的 Prompt 定义
每个 Agent 都是一个「专家」，只做自己最擅长的事

🌰 类比：一个基金研究团队：
    📊 行情分析师  → 只看数据和业绩
    📰 舆情研究员  → 只看新闻和市场情绪
    ⚠️  风险控制官  → 只评估风险等级
    📝 报告撰写员  → 汇总所有信息写报告

补充决策：
- Prompt 中明确告知每个 Agent「你不需要做什么」，防止越权和幻觉
- MARKET/SENTIMENT/RISK Prompt 通过 graph.py 的 .format(today=today) 注入当前日期
- REPORT_WRITER_PROMPT 的 {DATA_QUALITY_NOTICE} 占位符由 graph.py 在调用时替换
"""

